    private void btnPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarActionPerformed
        // TODO add your handling code here:
        ProdutoDb produtoDb = new ProdutoDb();
        String termo = txtPesquisa.getText();
        
        DefaultTableModel tabelModel = (DefaultTableModel) tblPesquisa.getModel();
        ResultSet produtos = produtoDb.selectAll(termo);
        
        try {
            while(produtos.next()) {
                String codigo = String.valueOf(produtos.getInt("codigo"));
                String nome = produtos.getString("nome");
                String preco = String.format("%.2f", produtos.getDouble("preco"));
                String estoque = String.valueOf( produtos.getInt("estoque"));
                
                String colunasProdutos[] = {codigo, nome, preco, estoque};
                
                tabelModel.addRow(colunasProdutos);
                tblPesquisa.setEnabled(false);
            }
        } catch (SQLException ex) {
            System.out.println("Erro na consulta: " + ex.getMessage());
        }
    }//GEN-LAST:event_btnPesquisarActionPerformed

